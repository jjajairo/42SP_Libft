#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	size_t	end;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1) - start;
	while (end && ft_strchr(set, s1[end + start]))
		end--;
	return (ft_substr(&s1[start], 0, end + 1));
}
