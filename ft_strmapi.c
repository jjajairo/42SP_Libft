#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			size;
	char			*new_s;
	unsigned int	i;

	if (!s)
		return (NULL);
	size = ft_strlen(s);
	new_s = (char *)malloc((sizeof(char) * size) + 1);
	if (!new_s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_s[i] = f(i, s[i]);
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
